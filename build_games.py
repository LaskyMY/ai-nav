# ── Game HTML templates ──
def G_REVEAL(btn_text, hidden_html):
    return f'<div class="mg-reveal" data-mg="reveal"><button class="mg-btn">{btn_text}</button><div class="mg-body">{hidden_html}</div></div>'

def G_QUIZ(question, opts, correct_idx, right_msg, wrong_msg):
    btns = ''.join(f'<button class="mg-btn">{o}</button>' for o in opts)
    return f'<div class="mg-quiz" data-mg="quiz" data-correct="{correct_idx}" data-right="{right_msg}" data-wrong="{wrong_msg}"><p style="font-size:12px;color:var(--tx2);margin-bottom:4px">{question}</p>{btns}<span class="mg-feedback"></span></div>'

def G_FLIP(front, back):
    return f'<div class="mg-flip" data-mg="flip"><div class="mg-flip-inner"><div class="mg-front">{front}</div><div class="mg-back">{back}</div></div></div>'

def G_SLIDER(stops):
    n = len(stops) - 1
    return f'<div class="mg-slider" data-mg="slider" data-stops=\'{json.dumps(stops)}\'><input type="range" min="0" max="{n}" value="{n//2}"><div class="mg-slider-label"></div></div>'

def G_MATCH(pairs, left_labels, right_labels):
    left = ''.join(f'<div class="mg-opt" data-idx="{i}">{l}</div>' for i,l in enumerate(left_labels))
    right = ''.join(f'<div class="mg-opt" data-idx="{i}">{r}</div>' for i,r in enumerate(right_labels))
    return f'<div class="mg-match" data-mg="match" data-pairs=\'{json.dumps(pairs)}\'><div class="mg-match-l">{left}</div><div class="mg-match-r">{right}</div><div class="mg-match-msg"></div></div>'

def G_SCENARIO(prompt, choices, outcomes):
    btns = ''.join(f'<button class="mg-btn" data-outcome="{o}">{c}</button>' for c,o in zip(choices, outcomes))
    return f'<div class="mg-scenario" data-mg="scenario"><p style="font-size:12px;color:var(--tx2);margin-bottom:4px">{prompt}</p>{btns}<div class="mg-outcome"></div></div>'

def G_COUNTER(steps):
    return f'<div class="mg-counter" data-mg="counter" data-steps=\'{json.dumps(steps)}\'><button class="mg-btn">+1</button><span class="mg-count">0</span><div class="mg-msg">{steps.get("0","开始点击")}</div></div>'

def G_SORT(categories, items):
    btns = ''.join(f'<button class="mg-btn">{c}</button>' for c in categories)
    return f'<div class="mg-sort" data-mg="sort" data-categories=\'{json.dumps(categories)}\' data-items=\'{json.dumps(items)}\'><div class="mg-sort-q"></div><div class="mg-sort-btns">{btns}</div><div class="mg-sort-score"></div></div>'

