# ── Injection logic ──
def inject_games(filepath, games):
    """Inject game HTML into cards in filepath based on games list of (card_num, html)."""
    with open(filepath, 'r', encoding='utf-8') as f:
        page = f.read()

    # Skip if games already injected (page already has data-mg in a card)
    if 'data-mg=' in page and 'mini-games.js' in page:
        return filepath

    game_map = {str(k): g for k, g in games}  # keys can be int or string

    # Scan character by character: find <div class="card">, count divs, inject before card's closing </div>
    TAG = '<div class="card">'
    CARD_NUM_RE = re.compile(r'<span class="card-num">(\d+)</span>')
    BADGE_RE = re.compile(r'<span class="(?:type-badge|sleep-badge)[^"]*">([^<]+)</span>')
    result = []
    pos = 0

    while True:
        idx = page.find(TAG, pos)
        if idx == -1:
            result.append(page[pos:])
            break

        # Everything before this card
        result.append(page[pos:idx])
        pos = idx  # start of <div class="card">

        # Scan forward counting <div and </div to find the matching closing tag
        depth = 0
        card_key = None
        i = pos
        card_start = pos
        while i < len(page):
            if page.startswith('<div', i):
                depth += 1
                i += 4
            elif page.startswith('</div>', i):
                depth -= 1
                if depth == 0:
                    # Found the card's closing </div>
                    card_body = page[card_start:i]
                    # Extract card identifier: card-num first, then badge text
                    m = CARD_NUM_RE.search(card_body)
                    if m:
                        card_key = m.group(1)  # e.g. "1", "6"
                    else:
                        m = BADGE_RE.search(card_body)
                        if m:
                            card_key = html.unescape(m.group(1))  # e.g. "A型", "7-8小时"
                    # Inject game if matched
                    if card_key and card_key in game_map:
                        result.append(card_body + game_map[card_key] + '</div>')
                    else:
                        result.append(page[card_start:i+6])
                    pos = i + 6  # skip past </div>
                    break
                i += 6
            else:
                i += 1

    new_html = ''.join(result)

    # Add CSS snippet before </style>
    if GAME_CSS.strip() not in new_html:
        new_html = new_html.replace('</style>', GAME_CSS + '\n</style>')

    # Add JS reference before </body>
    if 'mini-games.js' not in new_html:
        new_html = new_html.replace('</body>', '<script src="./mini-games.js"></script>\n</body>')

    # Fix bottom-nav: add 首页 link if missing (nav closes with </div>, not </nav>)
    if './index.html' not in new_html.split('bottom-nav')[1].split('</div>')[0] if 'bottom-nav' in new_html else True:
        new_html = new_html.replace(
            '</nav></div>',
            '</nav></div>',
        )
        # Actually: the bottom-nav is <div class="bottom-nav">...</div>
        # Match "百科</a></div>" and add 首页 before </div>
        new_html = new_html.replace(
            '百科</a></div>',
            '百科</a><a class="nav-btn" href="./index.html"><svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round"><path d="M3 9l9-7 9 7v11a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2z"/></svg>首页</a></div>'
        )

    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(new_html)

    return filepath

